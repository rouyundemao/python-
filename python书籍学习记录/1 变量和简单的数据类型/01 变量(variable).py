print('hello world')

message = 'hello python world'     # message（信息）是一个变量（variable）,'='右边则对应变量的值（value）
print(message)

# 变量可以随时修改，而 Python 将始终记录变量的最新值。
message = 'Hello Python Crash Course World!'
print(message)


# 练习
# 1、给变量赋予一个值答应出来。
sun = '今天是2025年10月15日，开始看python教程书籍！'
