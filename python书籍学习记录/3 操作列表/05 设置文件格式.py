
# 格式设置指南
# 要提出 Python 语言修改建议，需要编写 Python 增强提案（Python Enhancement Proposal，PEP）。PEP 8 是最古老的 PEP 之一，向 Python 程序员提供了代码格式设置指南。

# 缩进
# PEP 8 建议每级缩进都使用 4 个空格。这既可提高可读性，又留下了足够的多级缩进空间。

# 行长
# 很多 Python 程序员建议每行不超过 80 个字符。
# PEP 8 还建议注释的行长不超过 72 个字符，因为有些工具为大型项目自动生成文档时，会在每行注释开头添加格式化字符。

# 空行
# 要将程序的不同部分分开，可使用空行。
# 例如，如果有 5 行创建列表的代码，还有 3 行处理该列表的代码，用一个空行将这两部分隔开是合适的。








