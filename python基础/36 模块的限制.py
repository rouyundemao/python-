
# 模块的访问限制

# 1. __all__ 控制 "from 模块 import *" 能导入的内容
# 只有在 __all__ 列表中的名字才会被 * 导入。
# 详见 my_module.py 中的 __all__ = ['my_sum', 'test']

# 2. 以单下划线 _ 开头的变量/函数：约定为"内部使用"，不建议外部直接访问。
_internal_var = '内部变量，不建议外部使用'

def _internal_func():
    print('这是内部函数，不建议外部调用')

def public_func():
    print('这是公开函数，可以在外部调用')

# 3. 以双下划线 __ 开头的变量/函数（模块级别）：
#    与类中的名称改写不同，模块级别的双下划线变量不会被改写，
#    但同样属于"不建议外部直接使用"的约定。

# 4. __name__ 的作用：区分模块被导入还是直接运行。
#    当直接运行本文件时，__name__ == '__main__'
#    当本文件被其他文件导入时，__name__ == '36 模块的限制'

if __name__ == '__main__':
    public_func()
    print(f'__name__ = {__name__}')
